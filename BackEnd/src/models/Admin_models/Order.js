import mongoose from "mongoose";

const orderSchema = new mongoose.Schema({
    id: String,
    name:String,
    cid: String,
    category: String,
    frameColor:String,
    theme:String,
    size:String,
    customization: String,
    price:Number,
    status:{type:String,default:"new"}, 
    date: {
        type: Date,
        default: Date.now,
      }

});

const Order = mongoose.model('Order', orderSchema);
export default Order;
